
public class Main1 {

	public static void main(String[] args) {

		//Ta zmienna przechowuje typ Integer i przyjmuje wartość 5
		int int1 = 5;
		System.out.println(int1);

		//Ta zmienna przechowuje typ char i przyjmuje wartość 'x'
		char char2 = 'x';
		System.out.println(char2);

		//Ta zmienna przechowuje typ long i przyjmuje wartość 9223372036854775807
		long long3 = 9223372036854775807L;
		System.out.println(long3);

		//Ta zmienna przechowuje typ double i przyjmuje wartość 999.999
		double double4 = 999.999d;
		System.out.println(double4);

		//Ta zmienna przechowuje typ boolean i przyjmuje wartość false
		boolean boolean5 = false;
		System.out.println(boolean5);
	}

}
